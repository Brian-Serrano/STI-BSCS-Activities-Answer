var newWindow;
var w, h = 0;
var width = window.innerWidth;
var height = window.innerHeight;

function holidayFIC(){
	newWindow = open("fic.html", "fic", "width=640, height=480");
	w = width / 2;
	h = height / 2;
	newWindow.moveBy(w-300, h-200);
	newWindow.focus();
}

